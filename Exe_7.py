print("0 to 50:")
for x in range(0, 51, 1):
    print(x)

print("50 to 0:")
for x in range(50, -1, -1):
    print(x)

print("30 to 50:")
for x in range(30, 51, 1):
    print(x)

print("50 to 10 (by 2s):")
for x in range(50, 9, -2):
    print(x)

print("100 to 200 (by 5s):")
for x in range(100, 201, 5):
    print(x)
